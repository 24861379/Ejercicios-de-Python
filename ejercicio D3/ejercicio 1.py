""""
Escriba un script que solicite al usuario que ingrese la base y la altura 
del triángulo y calcule el área de este triángulo.
"""
print('Vamos a calcular el area de un triangulo \n Ingrese la base del triangulo')
base =int(input())

print('ingrese la altura del triangulo')
altura =int(input())

area =  base * altura / 2
print('el resutado del area del triangulo es: ', area)